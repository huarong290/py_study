# 二、默认参数
#默认参数也称为缺省参数,用于在定义函数时,为参数提供默认值,调用函数时,可以不传递有默认值的参数。
def reg_stu(name, age, gender, city='北京'):  #-------->默认值
    print(f"注册成功,姓名:{name},年龄:{age},性别:{gender},城市:{city}")

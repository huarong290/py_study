# 静态方法（@staticmethod）

# 使用场景：当你需要一个工具函数，和类、实例都无关时用静态方法，比如判断一个年龄是否合法，这个功能和具体的学生、班级都没关系，只是放在类里面组织代码。
# 大白话定义：相当于 “放在类里的工具函数”，和类、实例都没关系，只是借个地方放，比如 “判断年龄是否合法的工具”

class Student:
    # 类属性：所有学生共享的信息                        定义类属性时是在__init__外面的,所有对象共享的整体属性
    total_students = 0  # 班级总人数
    class_name = "苏州AI开发1班"  # 班级名称

    def __init__(self, name, age):
        # 实例属性：每个学生自己的信息
        self.name = name
        self.age = age
        # 每次创建学生对象，班级总人数加1
        Student.total_students += 1

    # 类方法：操作类属性，第一个参数是cls（代表类本身）与实例参数self同理
    @classmethod
    def get_total_students(cls):                   #  定义类方法得用@classmethod装饰
        """获取班级总人数"""                         #   第一个参数是cls（代表类本身）
        return f"班级总人数：{cls.total_students}人" #   可以访问类属性，不能访问实例属性
                                                  #    可以通过类名.方法名或实例名.方法名调用(更推荐类名调用)

    @classmethod
    def change_class_name(cls, new_name):  #     像这里的班级总人数,班级名称都是属于班级自己的属性,被称为类属性
        """修改班级名称"""                   #      而涉及班级自己属性的方法被称为 类方法
        cls.class_name = new_name          #     它们并不是学生对象的属性与方法,所以调用时用类名.方法名/属性名
        return f"班级名称已修改为：{cls.class_name}"


    # 静态方法：工具函数，没有默认参数，不能访问类属性和实例属性
    @staticmethod                              # 静态方法需要用 @ staticmethod装饰,没有默认参数
    def is_valid_age(age):                     # 不能访问类属性和实例属性,可以通过类名.方法名或实例名.方法名调用
        """判断年龄是否合法（0-120岁）"""
        return 0 < age < 120

    # __str__方法：对象的自我介绍
    def __str__(self):
        return f"学生：{self.name}，年龄：{self.age}，班级：{Student.class_name}"

# 1. 测试类属性和实例属性
student1 = Student("张三", 20)
student2 = Student("李四", 22)
print("类属性：", Student.total_students)  # 输出：2（两个学生）
print("实例属性：", student1.name)  # 输出：张三
print("实例属性：", student2.age)  # 输出：22

# 2. 测试类方法
print(Student.get_total_students())
print(Student.change_class_name("苏州AI开发进阶班"))

# 3. 测试静态方法
print(Student.is_valid_age(20))
print(Student.is_valid_age(150))
# 注意:  只有实例方法（没有装饰器、第一个参数强制是self的方法），才必须用实例对象调用。
#         而静态方法、类方法，都同时支持「类名调用」和「实例调用」，其中类名调用是更推荐的主流写法


# 4. 测试__str__方法

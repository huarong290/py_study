#----------------------------------装饰器------------------------------
# 定义: 装饰器就是给函数 “装壳”，不修改函数本身的代码，给函数增加额外功能。

# 常用于:
# 1.给 AI 模型调用函数加计时功能，统计接口响应时间（优化 AI 服务性能）
# 2.给接口函数加日志功能，记录调用参数、返回值（排查 AI 接口问题）
# 3.给重复调用的 AI 函数加缓存功能，避免重复请求模型（节省成本）
# 4.给敏感 AI 接口加权限验证功能，限制非法调用

# 无参函数模板:
'''
def 装饰器名称(func):
    def wrapper():
        # 1. 原函数执行前的额外功能（比如记录开始时间、权限校验）
        前置代码
        # 2. 执行原函数，保存返回值
        result = func()
        # 3. 原函数执行后的额外功能（比如记录结束时间、日志打印）
        后置代码
        # 4. 返回原函数的返回值，保证原函数功能不变
        return result
    # 5. 返回内层函数，替换原函数
    return wrapper
    '''
# 拓展2：常见的装饰器应用场景（套用模板就能写）
# 只要替换wrapper里的前置 / 后置代码，就能实现各种通用功能，比如：
# 1.日志记录：记录函数的调用时间、调用参数、返回值
# 2.权限校验：执行函数前判断用户是否有权限操作
# 3.接口重试：函数执行失败时自动重试指定次数
# 4.结果缓存：相同参数调用时，直接返回缓存的结果，不用重复执行

#------闭包-------
# 定义: 就是内层函数能 “记住” 外层函数的变量，即使外层函数执行完了，内层函数还能拿着这个变量用，
import time
# 1. 定义装饰器：统计函数运行时间
def time_decorator(func):       #这里的func就是被装饰的函数,后续ai_model_call会被传进来
    # 内层函数，用来添加额外功能
    def wrapper():
        # 额外功能：记录开始时间
        start_time = time.time()
        # 执行原函数
        result = func()      #把原函数的返回值，用result变量完整保存下来。  为什么要保存返回值？装饰器的核心原则是不改变原函数的功能，如果不保存并返回原函数的结果，用了装饰器之后，原函数的返回值就会丢失，直接破坏原函数的行为。
        # 额外功能：记录结束时间，计算耗时
        end_time = time.time()
        print(f"函数 {func.__name__} 运行耗时：{end_time - start_time:.2f}秒")   #这里func.__name__可以动态获取原函数的名称，让装饰器可以适配任意函数，不用硬编码函数名。
        # 返回原函数的结果，不改变原函数的返回值
        return result    #把之前保存的原函数返回值result，原封不动地返回出去。这是装饰器的“底线”：必须保证原函数的返回值能正常被调用方拿到，否则就违背了“不修改原函数功能”的设计原则。
    # 返回内层函数，替换原函数
    return wrapper

# 2. 使用@语法装饰函数（语法糖，等价于 ai_model_call = time_decorator(ai_model_call)）
@time_decorator
def ai_model_call():
    # 模拟调用AI模型的耗时操作（用已学的time.sleep）
    time.sleep(2)
    print("AI模型调用完成")
    return "AI模型返回结果"

# 3. 调用函数，自动执行装饰器的功能
result = ai_model_call()
print(f"函数返回值：{result}")